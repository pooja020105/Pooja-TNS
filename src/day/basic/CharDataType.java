package day.basic;

public class CharDataType {

	public static void main(String[] args) {
		char ch = 'r';
		System.out.println(ch);

		char ch1 = 26;
		System.out.println(ch1);

		char var1 = '\uABCD';
		System.out.println(var1);

		char var2 = '\uB77c';
		System.out.println(var2);

		int a = 'P';
		System.out.println(a);

	}

}
